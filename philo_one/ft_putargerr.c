#include "philo_one.h"

int	ft_putargerr(void)
{
	ft_puterr(ARGERR);
	ft_puterr(ARG1);
	ft_puterr(ARG2);
	ft_puterr(ARG3);
	ft_puterr(ARG4);
	ft_puterr(ARG5);
	return (1);
}
